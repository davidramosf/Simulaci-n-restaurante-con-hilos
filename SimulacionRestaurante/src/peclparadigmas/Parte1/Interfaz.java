    public void actualizar() throws InterruptedException {
        String msgMostradorPedidos = "";
        String msgMesaPlatos = "";

        while (true) {
            Thread.sleep(5);
            try {
                for (int i = 0; i < mostradorPedidos.getListaPedidos().size() - 1; i++) {

                    msgMostradorPedidos += mostradorPedidos.getListaPedidos().get(i) + ", ";

                }
                jTextFieldContenidoMostradorPedido.setText(msgMostradorPedidos);
                msgMostradorPedidos = "";

                for (int j = 0; j < mesaPlatos.getListaPedidos().size() - 1; j++) {

                    msgMesaPlatos += mesaPlatos.getListaPedidos().get(j) + ", ";

                }

                jTextFieldContenidoMesaPlatos.setText(msgMesaPlatos);
                msgMesaPlatos = "";

                jTextFieldCocinero1.setText(c1.getPedido());
                jTextFieldCocinero2.setText(c2.getPedido());
                jTextFieldCocinero3.setText(c3.getPedido());
                jTextFieldEmpleado1.setText(e1.getPedido());
                jTextFieldEmpleado2.setText(e2.getPedido());
            } catch (Exception e) {
                System.out.println(e);

            }

        }
    }
