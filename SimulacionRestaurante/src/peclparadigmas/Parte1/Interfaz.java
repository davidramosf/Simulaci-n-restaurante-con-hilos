    private void jToggleButtonPausarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonPausarTodoActionPerformed

        if (estadoPT) {
            mostradorPedidos.cerrar();
            mesaPlatos.cerrar();
            estadoPT = !estadoPT;
        } else {
            mostradorPedidos.abrir();
            mesaPlatos.abrir();
            estadoPT = !estadoPT;
        }


    }//GEN-LAST:event_jToggleButtonPausarTodoActionPerformed

